    private void jbtCarpeta1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jbtCarpeta1MouseClicked
        
        /**
         * Hace que jfc sea visible
         * Y que en jfc se puedan seleccionar archivos y directorios
         */
        this.jfc.setVisible(true);
        this.jfc.setFileSelectionMode(jfc.FILES_AND_DIRECTORIES);
        
        int result = this.jfc.showOpenDialog(this);

        /**
         * Si no se ha cancelado la busqueda...
         */
        if (result != jfc.CANCEL_OPTION) {

            /**
             * ...creamos un objeto de tipo File
             * Y recoge la ruta del archivo o directorio seleccionado
             */
            File fileName = this.jfc.getSelectedFile();

            /**
             * Si la ruta es nula o está vacía...
             */
            if ((fileName == null) || (fileName.getName().equals(""))) {
                
                /**
                 * ..se muestra en el buscador lo siguiente
                 */
                this.jtfBuscador.setText("...");
            } else { //Sino...
                
                /**
                 * Se mostrará en el buscador la ruta del archivo o directorio
                 */
                this.jtfBuscador.setText(fileName.getAbsolutePath());
            }
        }
    }//GEN-LAST:event_jbtCarpeta1MouseClicked

    private void jbtCarpeta2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jbtCarpeta2MouseClicked
        
        /**
         * Este método funciona igual que el anterior
         * Pero aquí filtraremos los archivos que termine en Mp3 o mp3
         */
        this.jfc.setVisible(true);
        this.jfc.setFileSelectionMode(jfc.FILES_AND_DIRECTORIES);

        FileNameExtensionFilter imgFilter = new FileNameExtensionFilter("Mp3", "mp3");
        this.jfc.setFileFilter(imgFilter);
        int result = this.jfc.showOpenDialog(this);

        if (result != jfc.CANCEL_OPTION) {

            File fileName = this.jfc.getSelectedFile();

            if ((fileName == null) || (fileName.getName().equals(""))) {
                this.jtfRuta.setText("...");
            } else {
                this.jtfRuta.setText(fileName.getAbsolutePath());
            }
        }
    }//GEN-LAST:event_jbtCarpeta2MouseClicked

