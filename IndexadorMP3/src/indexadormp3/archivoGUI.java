    private void jl1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jl1MouseClicked
        
        /**
         * En selección guardaremos el valor del elemento de la lista que hayamos seleccionado
         * Y se guardarán en un array de tipo String
         * Se ha elegido un array normal porque se puede utilizar el método split()
         */
        String seleccion = jl1.getSelectedValue();
        String[] array = seleccion.split(" - ");
        
        /**
         * Añadimos a canciones seleccionadas el nombre y el nombre del artista por separado
         */
        cancionesSeleccionadas.add(g.buscarCancion(array[0], array[1]));

        /**
         * Se borra el elemento de jl1
         * Se añade el elemento a jl2
         */
        model1.removeElement(seleccion);
        model2.addElement(seleccion);
    }//GEN-LAST:event_jl1MouseClicked

    private void jl2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jl2MouseClicked
        
        /**
         * Este método funciona igual que el anterior pero borrando de jl2 y añadiendo a jl1
         * Además en vez de añadir a cancionesSeleccionadas se borrará la canción que hayamos seleccionado
         */
        String seleccion = jl2.getSelectedValue();
        String[] array = seleccion.split(" - ");
        int indice = jl2.getSelectedIndex();
        
        cancionesSeleccionadas.remove(indice); 
        
        model2.removeElement(seleccion);
        model1.addElement(seleccion);
    }//GEN-LAST:event_jl2MouseClicked

