    /**
     * Este método sirve para rellenar el JList jl1
     * @param list: aquí le pasamos la lista de las canciones que queremos introducir en jl1
     */
    public void rellenarLista(ArrayList<Mp3> list) {
        
        /**
         * Recorremos la lista de las canciones
         */
        for (int i = 0; i < list.size(); i++) {
            
            /**
             * Y añadimos al modelo los elementos
             * Quedará como: 
             * Nombre Canción - Nombre Artista
             */
            model1.addElement(list.get(i).getNombre() + " - " + list.get(i).getArtista());
        }
        jl1.setModel(model1);
    }

